    public void timeb() {
        new Timer(10, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int value = pgb.getValue();
                if (value < pgb.getMaximum()) {
                    pgb.setValue(value + 1);
                } else {
                    ChaoJDialog.this.dispose();
                }
            }
        }) {
        }.start();
    }


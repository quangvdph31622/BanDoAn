    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        int index = tblNhanVien.getSelectedRow();
        setModel(qlnv.getAllNhanVien(txttimkiem.getText()).get(index));
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        if (!Auth.isManager()) {
            JOptionPane.showMessageDialog(this, "Bạn không có quyền xóa");
        } else {
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa");
            if (chon != JOptionPane.YES_OPTION) {
                return;
            }
            qlnv.delete(Integer.parseInt(txtmanv.getText()));
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            fillTable();
        }
    }//GEN-LAST:event_btnxoaActionPerformed


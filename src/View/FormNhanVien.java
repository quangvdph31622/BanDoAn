    public void initcbo() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbocv.getModel();
        model.removeAllElements();

        // Lấy danh sách chức vụ
        List<ChucVu> chucVuList = qlcv.getAll();

        // Thêm từng chức vụ vào combobox
        for (ChucVu c : chucVuList) {
            model.addElement(c);
        }

        // Nếu danh sách chức vụ không rỗng, set giá trị đầu tiên cho combobox
        if (!chucVuList.isEmpty()) {
            cbocv.setSelectedItem(chucVuList.get(0));
        }
    }

    public void initTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        String[] col = new String[]{"Mã nv", "Tên nv", "Giới tính", "Địa chỉ", "Ngày sinh", "Chức vụ", "Ảnh", "Tài khoản", "Mật khẩu", "Ngày tạo"};
        model.setColumnIdentifiers(col);
    }

    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        String kw = txttimkiem.getText();
        List<NhanVienFull> list = qlnv.getAllNhanVien(kw);
        for (int i = 0; i < list.size(); i++) {
            NhanVienFull nv = list.get(i);
            Object[] row = new Object[]{nv.getNv().getMaNV(), nv.getNv().getTenNV(), nv.getNv().isGioiTinh() ? "Nam" : "Nữ", nv.getNv().getDiaChi(), nv.getNv().getNgaySinh(), nv.getNv().getC(), nv.getNv().getImage(), nv.getTk().getTaiKhoan(), nv.getTk().getMatKhau(), nv.getTk().getNgayTao()};
            model.addRow(row);

        }
    }

    public NhanVienFull getModel() {
        NhanVienFull nv = new NhanVienFull();
        nv.setNv(new NhanVien());
        nv.setTk(new TaiKhoan());
        nv.getNv().setMaNV(Integer.parseInt(txtmanv.getText()));
        nv.getNv().setTenNV(txttennv.getText());
        if (rdonam.isSelected()) {
            nv.getNv().setGioiTinh(true);
        } else {
            nv.getNv().setGioiTinh(false);
        }
        nv.getNv().setDiaChi(txtdiachi.getText());
        nv.getNv().setNgaySinh(XDate.convertDate(txtngaysinh.getText(), "yyyy-MM-dd"));
        if (srcanh == null) {
            nv.getNv().setImage("null");
        } else {
            nv.getNv().setImage(srcanh);
        }
        List<ChucVu> list = qlcv.getAll();
        if (cbocv.getSelectedItem() != null) {
            for (ChucVu c : list) {
                if (cbocv.getSelectedItem().toString().equalsIgnoreCase(c.getTenCV())) {
                    nv.getNv().setC(c);
                }
            }
        }
        nv.getTk().setTaiKhoan(txttk.getText());
        nv.getTk().setMatKhau(String.valueOf(txtmk.getPassword()));
        if (nv.getTk().getNgayTao() != null) {
            nv.getTk().setNgayTao(nv.getTk().getNgayTao());
        } else {
            nv.getTk().setNgayTao(new Date());
        }
        return nv;
    }

    public void setModel(NhanVienFull nv) {
        Integer index = tblNhanVien.getSelectedRow();
        txtmanv.setText(tblNhanVien.getValueAt(index, 0).toString());
        txttennv.setText(tblNhanVien.getValueAt(index, 1).toString());
        txtngaysinh.setText(tblNhanVien.getValueAt(index, 4).toString());
        if (tblNhanVien.getValueAt(index, 2).toString().equals("Nam")) {
            rdonam.setSelected(true);
        } else {
            rdonu.setSelected(true);
        }
        txtdiachi.setText(tblNhanVien.getValueAt(index, 3).toString());
        updateImage(nv.getNv().getImage());
        cbocv.setSelectedItem(nv.getNv().getC());
        txttk.setText(tblNhanVien.getValueAt(index, 7).toString());
        txtmk.setText(tblNhanVien.getValueAt(index, 8).toString());
    }


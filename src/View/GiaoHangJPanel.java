    private void tblDanhsachspMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDanhsachspMouseClicked
        // TODO add your handling code here:
        int point = tblDanhsachsp.getSelectedRow();

        SanPhamDTO selectedSP = listSP.get(point);

        // Check if the item is already in the cart
        boolean found = false;
        for (GioHangDTO gioHangDTO : GioDTO) {
            if (gioHangDTO.getMaSanPham() == selectedSP.getMaSanPham()) {
                gioHangDTO.setSoLuong(gioHangDTO.getSoLuong() + 1);
                found = true;
                break;
            }
        }

        // If the item is not in the cart, add a new entry
        if (!found) {
            GioHangDTO gioHangDTO = new GioHangDTO();
            gioHangDTO.setMaSanPham(selectedSP.getMaSanPham());
            gioHangDTO.setTenSanPham(selectedSP.getTenSanPham());
            gioHangDTO.setTenLoai(selectedSP.getTenLoai());
            gioHangDTO.setSize(selectedSP.getSize());
            gioHangDTO.setTrangThai(selectedSP.getTrangThai());
            gioHangDTO.setGia(selectedSP.getGia());
            gioHangDTO.setMoTa(selectedSP.getMoTa());
            gioHangDTO.setSoLuong(1);
            GioDTO.add(gioHangDTO);
        }

        // Update the cart display
        fillGioHang(GioDTO);

    }//GEN-LAST:event_tblDanhsachspMouseClicked


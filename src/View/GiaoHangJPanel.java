    public void setModelpgh(PhieuGiaoHang pgh) {
        txtMaPhieuGiao1.setText(pgh.getMaPhieuGiao() + "");
        mapg1 = pgh.getMaPhieuGiao();
        txtTenKH.setText(pgh.getTenKH());
        txtNVNhanDon.setText(pgh.getMaNhanVien() + "");
        txtNhanVienGiaoHang.setText(pgh.getMaNhanVien1() + "");
        txtMaSP.setText(pgh.getMaSanPham() + "");
        txtTongTien.setText(pgh.getSoLuong() + "");
        txtDiaChiGiao.setText(pgh.getDiaChiGiaoHang());
        txtTimeDuKien.setText(pgh.getGiaoDuKien() + "");
        txtGiaoHoanThanh.setText(pgh.getGiaoThucTe() + "");
        if (pgh.getTrangThai().equals("Đang Chuẩn Bị")) {
            cboTrangThai.setSelectedIndex(0);
        } else if (pgh.getTrangThai().equals("Đã Giao Hàng")) {
            cboTrangThai.setSelectedIndex(1);
        } else if (pgh.getTrangThai().equals("Đang Vận Chuyển")) {
            cboTrangThai.setSelectedIndex(2);
        } else if (pgh.getTrangThai().equals("Thất Lạc Hàng")) {
            cboTrangThai.setSelectedIndex(3);
        } else {
            cboTrangThai.setSelectedIndex(4);
        }
        txtXacNhanKhachHang.setText(pgh.getKhachHangXacNhan() == 1 ? "Đã Nhận Hàng" : "Chưa Nhận Hàng");
        txtGhiChu.setText(pgh.getGhiChu());
        txtTongTien.setText(pgh.getTenKH());
        txtPhiVanChuyen1.setText(pgh.getPhiVanChuyen() + "");
        txtTienKhachDuaDSPG.setText(pgh.getTienKhachDua() + "");
    }


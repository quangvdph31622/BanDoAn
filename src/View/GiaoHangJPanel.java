    public PhieuGiaoHang getModelpgh() {
        PhieuGiaoHang pgh1 = new PhieuGiaoHang();
        pgh.setMaPhieuGiao(Integer.parseInt(txtMaPhieuGiao1.getText()));
        if (cboTrangThai.getSelectedIndex() == 0) {
            pgh.setTrangThai("Đang Chuẩn Bị");
        } else if (cboTrangThai.getSelectedIndex() == 1) {
            pgh.setTrangThai("Đã Giao Hàng");

        } else if (cboTrangThai.getSelectedIndex() == 2) {
            pgh.setTrangThai("Đang Vận Chuyển");
        } else if (cboTrangThai.getSelectedIndex() == 3) {
            pgh.setTrangThai("Thất Lạc Hàng");
        } else {
            pgh.setTrangThai("Đang Tạo");
        }
        return pgh1;
    }


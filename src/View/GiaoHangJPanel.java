    private void tblGioHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGioHangMouseClicked
        // TODO add your handling code here:
        int point = tblGioHang.getSelectedRow();
        int tb = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa sản phẩm này không ?", "Xác nhận", JOptionPane.OK_CANCEL_OPTION);
        if (tb == JOptionPane.OK_OPTION) {
            GioHangDTO selectedGH = GioDTO.get(point);

            // Use iterator to safely remove the item while iterating
            Iterator<GioHangDTO> iterator = GioDTO.iterator();
            while (iterator.hasNext()) {
                GioHangDTO gioHangDTO = iterator.next();
                if (gioHangDTO.getMaSanPham() == selectedGH.getMaSanPham()) {
                    iterator.remove(); // Remove the current item from the list
                }
            }

            // Update the cart display after removal
            fillGioHang(GioDTO);
        }
    }//GEN-LAST:event_tblGioHangMouseClicked

